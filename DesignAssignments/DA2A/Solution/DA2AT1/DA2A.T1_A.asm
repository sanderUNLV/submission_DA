.include<m328pdef.inc>
.INCLUDE "DELAY_OFF_ON.asm"

.ORG 0x00
	
	LDI R16, 0x3C	;LOAD THE VALUE 0x02 0000 0010
	OUT DDRB, R16	;SETS PB.2-5 AS OUTPUTS
TOGGLE:
	LDI R16, 0x38
	OUT PORTB, R16	;'TURNS ON' POTB HIGH STATE
	RCALL DELAY_OFF
	LDI R16, 0x3C
	OUT PORTB, R16	;'TURNS OFF' PORTB LOW STATE
	RCALL DELAY_ON
	RJMP TOGGLE		;GO TO 'TOGGLE:'


