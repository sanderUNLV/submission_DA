.include <m328pdef.inc>
.include "DELAY.asm"

.ORG 0x000000

	LDI R16, 0x3C	;LOAD THE VALUE 0x3C INTO R16
	OUT DDRB, R16	;SETS PB.2-5 AS OUTPUTS, 00111100
CHECK: 
	SBIC PINC,2 ;skip next if PB2 is clear , IF BUTTON IS PUSHED GO TO "LDI R16, 0x38"
	RJMP SKIP ;GO TO 'SKIP:'
	LDI R16, 0x38 ;LOAD THE VALUE 0x38 INTO R16
	OUT PORTB, R16	;'TURNS ON' PORTB...REVERSE LOGIC
	RCALL DELAY
SKIP:  
	LDI R16, 0x3C ;LOAD THE VALUE 0x3C INTO R16
	OUT PORTB, R16	;'TURNS OFF' PORTB...REVERSE LOGIC
	RJMP CHECK ;GO TO 'CHECK:'
